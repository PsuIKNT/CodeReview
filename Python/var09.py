'''Дано число D и вершина A1 непустого стека. Добавить элемент со значением D в стек и вывести ссылку A2 на новую вершину стека.'''
class Node:
    def __init__(self, data, next=None):
        self.Data = data
        self.Next = next
def stack(A1, D):  # Функция для добавления нового элемента в стек
    A2 = Node(D, A1)  # Создаем новый узел, который ссылается на текущую вершину A1
    return A2  # Возвращаем новую вершину стека

def iput():
    values = input("Введите элементы стека через пробел: ").split()
    A1 = None  # Начинаем с пустого стека
    for value in reversed(values):  # Обрабатываем введенные значения с конца (чтобы первый введённый элемент стал вершиной)
        A1 = Node(int(value), A1)  # Создаем новый узел и связываем его с текущей вершиной
    return A1  # Возвращаем ссылку на вершину стека

A1 = iput()
D = int(input("Введите число D для добавления в стек: "))
A2 = stack(A1, D)  # Добавляем новый элемент в стек

print("Новый стек:")
current = A2  # Начинаем с вершины A2
while current:  # Пока не достигли конца стека (None)
    print(current.Data, end=" → ")  # Выводим значение текущего узла
    current = current.Next  # Переходим к следующему узлу
print("None")  # Завершаем вывод

'''Введите элементы стека через пробел: 2 3 4 5 
Введите число D для добавления в стек: 1
Новый стек:
1 → 2 → 3 → 4 → 5 → None'''